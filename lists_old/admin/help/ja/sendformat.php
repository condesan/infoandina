メッセージをHTMLフォーマットで作成しているなら、送信形式を指定してください。<br />
<ul>
<li><b>HTML</b> - HTMLで電子メールを受信したいと指定したユーザにはHTMLメール、その他すべてのユーザにはテキストメール</li>
<li><b>text</b> - すべてのユーザにテキストメールのみ</li>
<li><b>text and HTML</b> - HTMLメールとテキストメールの両方のフォーマットをもつ大きなサイズの電子メール（大きなサイズの電子メールですが、ほとんどのユーザで利用できそうです）</li>
<li><b>PDF</b> - PDF添付ファイル</li>
<li><b>text and PDF</b> - PDF添付ファイルをもつテキストメール</li>
</ul>

<b>注目:</b> PDFバージョンはテキストメッセージの変換されたもので、HTMLメッセージの変換されたものではありません。